# Strings
name, surname, age = "Kendra", "Contreras", 27
print('Mi name es {} {} y mi edad es {}'.format(name, surname, age))
print(f"Mi nombre es {name} {surname} y mi edad es {age}")

# Slice
name = "kendra contreras"
name_slice = name[-2]
print(name_slice)  # a

# Reverse
reverse_name = name[::-1]
print(reverse_name)  # sarertnoC ardneK

# Funciones
print(name.capitalize())
print(name.upper())
print(name.count('e'))  # 2
